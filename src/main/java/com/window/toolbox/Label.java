package com.window.toolbox;

public class Label {
  
	String forW;
	String text;
	public Label(String forW, String text) {
		this.forW = forW;
		this.text = text;
	}
	public String getForW() {
		return forW;
	}
	public void setForW(String forW) {
		this.forW = forW;
	}
	public String getText() {
		return text;
	}
	public void setText(String text) {
		this.text = text;
	}
	
}
